package sample;

public class CustomMethods {
	
	public void test1() {

		System.out.println("Name is none");

	}
	

	public void test1(String name) {

		System.out.println(name);

	}

	public void test1(String name, String name2) {

		System.out.println(name + name2);

	}

	public void test1(String name, String name2, String name3) {

		System.out.println(name + name2+ name3);

	}

	public void test1(String name, String name2, String name3, String name4) {

		System.out.println(name + name2+ name3+ name4);

	}

	public void test1(String name, String name2, String name3, String name4, String name5) {

		System.out.println(name + name2+ name3+ name4+ name5);

	}


}
